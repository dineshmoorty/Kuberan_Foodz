function interpolate(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) =>
    params[key] !== undefined ? params[key] : `{${key}}`
  );
}

function getLanguage() {
  const saved = localStorage.getItem(KUBERAN_LANGUAGE_KEY);
  return saved === "ta" ? "ta" : "en";
}

function setLanguage(language) {
  const nextLanguage = language === "ta" ? "ta" : "en";
  localStorage.setItem(KUBERAN_LANGUAGE_KEY, nextLanguage);
  applyTranslations();
  window.dispatchEvent(
    new CustomEvent("kuberan-language-change", {
      detail: { language: nextLanguage },
    })
  );
}

function t(key, params = {}) {
  const language = getLanguage();
  const value =
    KUBERAN_TRANSLATIONS[language]?.[key] ?? KUBERAN_TRANSLATIONS.en[key] ?? key;

  return interpolate(value, params);
}

function setText(element, key, params = {}) {
  if (element) {
    element.textContent = t(key, params);
  }
}

function setAttr(element, attribute, key, params = {}) {
  if (element) {
    element.setAttribute(attribute, t(key, params));
  }
}

function renderLanguageSwitchers() {
  const language = getLanguage();

  document.querySelectorAll("[data-language-switcher]").forEach((switcher) => {
    switcher.innerHTML = `
      <div class="language-switcher inline-flex items-center rounded-full border border-white/30 bg-white/10 p-1 text-white">
        <button
          type="button"
          data-language-option="en"
          class="rounded-full px-3 py-1 text-xs font-semibold transition ${language === "en" ? "bg-white text-[#7A3D12]" : "text-white"}"
        >
          ${t("switcher.en")}
        </button>
        <button
          type="button"
          data-language-option="ta"
          class="rounded-full px-3 py-1 text-xs font-semibold transition ${language === "ta" ? "bg-white text-[#7A3D12]" : "text-white"}"
        >
          ${t("switcher.ta")}
        </button>
      </div>
    `;
  });

  document.querySelectorAll("[data-language-option]").forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.getAttribute("data-language-option"));
    });
  });
}

